package pl.eg.enginegame;

public class TurnOperation {
    private int unitID;
    private int toX;
    private int toY;
    private String unitName;

    public int getUnitID() {
        return unitID;
    }

    public int getToX() {
        return toX;
    }

    public int getToY() {
        return toY;
    }

    public String getUnitName() {
        return unitName;
    }
}

